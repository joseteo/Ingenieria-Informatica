    @Override
    public void paint(Graphics g){
        super.paint(g);
        Graphics2D g2d = (Graphics2D)g;
        g2d.setPaint(color);
        for(Shape s:vShape){
            if (relleno)
                g2d.fill(forma);
            g2d.draw(s);
        }
    }
    
